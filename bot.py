COUNTRIES_PER_PAGE = 20

def init_db():
    conn = sqlite3.connect('bot_data.db', check_same_thread=False)
    cursor = conn.cursor()
    cursor.execute('''CREATE TABLE IF NOT EXISTS accounts (api_key TEXT PRIMARY KEY, username TEXT)''')
    cursor.execute('''CREATE TABLE IF NOT EXISTS settings (key TEXT PRIMARY KEY, value TEXT)''')
    cursor.execute('''CREATE TABLE IF NOT EXISTS selected_countries (id TEXT PRIMARY KEY)''')
    defaults = [
        ('prefixes', ''), ('channel_id', ''), ('status', 'off'),
        ('service', 'wa'), ('sessions', 'all'), ('banned', 'all'),
        ('project_id', '')
    ]
    for k, v in defaults:
        cursor.execute("INSERT OR IGNORE INTO settings (key, value) VALUES (?, ?)", (k, v))
    conn.commit()
    return conn

db = init_db()

def get_set(key):
    with db_lock:
        cursor = db.cursor()
        cursor.execute("SELECT value FROM settings WHERE key=?", (key,))
        res = cursor.fetchone()
        return res[0] if res else ""

def set_set(key, val):
    with db_lock:
        cursor = db.cursor()
        cursor.execute("UPDATE settings SET value=? WHERE key=?", (val, key))
        db.commit()

def db_execute(query, params=()):
    with db_lock:
        db.execute(query, params)
        db.commit()

def db_fetchall(query, params=()):
    with db_lock:
        cursor = db.cursor()
        cursor.execute(query, params)
        return cursor.fetchall()

def db_fetchone(query, params=()):
    with db_lock:
        cursor = db.cursor()
        cursor.execute(query, params)
        return cursor.fetchone()
